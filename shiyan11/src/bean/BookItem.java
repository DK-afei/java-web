package bean;

public class BookItem {
	private String shuming = null;
	private float danjia = 0;
	private int shuliang = 0;
	private float zongjia = 0;
	/**
	 * @return the shuming
	 */
	public String getShuming() {
		return shuming;
	}
	/**
	 * @param shuming the shuming to set
	 */
	public void setShuming(String shuming) {
		this.shuming = shuming;
	}
	/**
	 * @return the danjia
	 */
	public float getDanjia() {
		return danjia;
	}
	/**
	 * @param danjia the danjia to set
	 */
	public void setDanjia(float danjia) {
		this.danjia = danjia;
	}
	/**
	 * @return the shuliang
	 */
	public int getShuliang() {
		return shuliang;
	}
	/**
	 * @param shuliang the shuliang to set
	 */
	public void setShuliang(int shuliang) {
		this.shuliang = shuliang;
	}
	/**
	 * @return the zongjia
	 */
	public float getZongjia() {
		return zongjia;
	}
	/**
	 * @param zongjia the zongjia to set
	 */
	public void setZongjia(float zongjia) {
		this.zongjia = zongjia;
	}
	
	}
